package com.tianle.model.logic;

public class LogicFriend {
	private String id;
	private String name;
	private String head;
	private String friendClass;
	private String college;
	private int circlesCounts;

	public String getId() {
		return id;
	}

	public void setId(String id) {
		this.id = id;
	}

	public String getName() {
		return name;
	}

	public void setName(String name) {
		this.name = name;
	}

	public String getHead() {
		return head;
	}

	public void setHead(String head) {
		this.head = head;
	}

	public String getFriendClass() {
		return friendClass;
	}

	public void setFriendClass(String friendClass) {
		this.friendClass = friendClass;
	}

	public String getCollege() {
		return college;
	}

	public void setCollege(String college) {
		this.college = college;
	}

	public int getCirclesCounts() {
		return circlesCounts;
	}

	public void setCirclesCounts(int circlesCounts) {
		this.circlesCounts = circlesCounts;
	}

}
